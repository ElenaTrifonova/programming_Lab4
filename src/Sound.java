/**
 * Class of sounds
 */
public class Sound {
    /**
     * method that describes sound of stalagmite destroying
     */

    public void soundStalakt() {
        System.out.println("There was a crackling sound from breaking stalagmite.");
        System.out.println("\n");
    }

    /**
     * method that describes sound of tunnel destroying
     */

    public void soundTunnel() {
        System.out.println("There was a terrible loud sound of falling stones.");
        System.out.println("\n");
    }

    /**
     * method that describes sound of clothes falling
     */

    public void soundClothes() {
        System.out.println("There was a rustling not loud sound of falling clothes.");
        System.out.println("\n");
    }
}